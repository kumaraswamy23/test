package com.ping.admin.api.adaptermodel;

public class AdapterPayload {

	public String id;
	public String name;
	public String pcvId;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPcvId() {
		return pcvId;
	}
	public void setPcvId(String pcvId) {
		this.pcvId = pcvId;
	}
	
	
}
