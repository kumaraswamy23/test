package com.ping.admin.api.policymodel;

public class PolicyPayload {

	public String selectorId;
	public String adapterid;
	public String policyContractId;
	public String policyId;
	public String policyName;
	public String authAppAPIId;
	public String getSelectorId() {
		return selectorId;
	}
	public void setSelectorId(String selectorId) {
		this.selectorId = selectorId;
	}
	public String getAdapterid() {
		return adapterid;
	}
	public void setAdapterid(String adapterid) {
		this.adapterid = adapterid;
	}
	public String getPolicyContractId() {
		return policyContractId;
	}
	public void setPolicyContractId(String policyContractId) {
		this.policyContractId = policyContractId;
	}
	public String getPolicyId() {
		return policyId;
	}
	public void setPolicyId(String policyId) {
		this.policyId = policyId;
	}
	public String getPolicyName() {
		return policyName;
	}
	public void setPolicyName(String policyName) {
		this.policyName = policyName;
	}
	public String getAuthAppAPIId() {
		return authAppAPIId;
	}
	public void setAuthAppAPIId(String authAppAPIId) {
		this.authAppAPIId = authAppAPIId;
	}
	
	
}
